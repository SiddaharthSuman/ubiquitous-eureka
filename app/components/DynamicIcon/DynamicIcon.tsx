interface DynamicIconProps extends React.HTMLAttributes<HTMLDivElement> {
  active: boolean;
  iconName: string;
}

const DynamicIcon = ({ active, iconName, ...rest }: DynamicIconProps) => {
  const IconComponent = filledIconComponents[iconName];
  const OutlineIconComponent = outlineIconComponents[iconName];

  if (!IconComponent || !OutlineIconComponent) {
    console.log(`Icon not found for name: ${iconName}`);
    return null;
  }

  return (
    <Box className={styles.iconContainer} {...rest}>
      <Box className={`${styles.iconFilled} ${active ? styles.active : ''}`}>
        <IconComponent />
      </Box>
      <Box className={`${styles.iconOutline} ${!active ? styles.active : ''}`}>
        <OutlineIconComponent />
      </Box>
    </Box>
  );
};

